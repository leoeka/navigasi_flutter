class Item {
  final String name;
  final int price;
  final String photo;
  final double rating;
  final int stock;
  final String nim;  // Menyimpan NIM
  final String nama; // Menyimpan Nama

  Item({
    required this.name,
    required this.price,
    required this.photo,
    required this.rating,
    required this.stock,
    required this.nim,   // Properti NIM
    required this.nama,  // Properti Nama
  });
}
